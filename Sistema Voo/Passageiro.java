public class Passageiro {
    private String nome;
    private int idade;
    private String cpf;

    public void setNome (String nome) {
        this.nome = nome;
    }
    
    public void setIdade (int idade) {
        this.idade = idade;
    }

    public void setCPF (String cpf) {
        this.cpf = cpf;
    }

    public int getIdade() {
        return idade;
    }

    public String getNome() {
        return nome;
    }

    public String getCpf() {
        return cpf;
    }
}



    